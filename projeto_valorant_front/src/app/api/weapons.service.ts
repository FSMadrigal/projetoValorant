  private comparar(a: any, b: any) {
    const armas: any = {
        classic: 0,
        shorty: 1,
        frenzy: 2,
        ghost: 3,
        sheriff: 4,
        stinger: 5,
        spectre: 6,
        bucky: 7,
        judge: 8,
        bulldog: 9,
        guardian: 10,
        phantom: 11,
        vandal: 12,
        marshal: 13,
        operator: 14,
        ares: 15,
        odin: 16,
    }

    const stringDataA = a.release_date;
    const stringDataB = b.release_date;

    const diaDataA = stringDataA.substring(3, 5);
    const diaDataB = stringDataB.substring(3, 5);
    const mesDataA = stringDataA.substring(0, 2);
    const mesDataB = stringDataB.substring(0, 2);
    const anoDataA = stringDataA.substring(6);
    const anoDataB = stringDataB.substring(6);

    const dataA = new Date(+anoDataA, +mesDataA-1, +diaDataA);
    const dataB = new Date(+anoDataB, +mesDataB-1, +diaDataB);

    if (dataA.getTime() < dataB.getTime()) {
        return 1;
    }
    if (dataA.getTime() > dataB.getTime()) {
        return -1;
    }
    if (a.collection < b.collection) {
        return -1;
    }
    if (a.collection > b.collection) {
        return 1;
    }
    if (armas[a.weapon] < armas[b.weapon]) {
        return -1;
    }
    if (armas[a.weapon] > armas[b.weapon]) {
        return 1;
    }
    return 0;

  }

  getAllWeapons() {
    return this.weapons.slice(0, 20);
  }

  filterAllWeaponsByName(weaponName: string) {
    return this.weapons.filter(weapon => weapon.name.includes(weaponName)).slice(0, 20);
  }

  filterAllWeaponsByNameWeaponCollection(search: string) {
    const filterByName = this.weapons.filter(weapon => weapon.name.includes(search));
    const filterByWeapon = this.weapons.filter(weapon => weapon.weapon.includes(search));
    const filterByCollection = this.weapons.filter(weapon => weapon.collection.includes(search));

    const filterSet = new Set();
    filterByName.forEach(filterSet.add, filterSet);
    filterByWeapon.forEach(filterSet.add, filterSet);
    filterByCollection.forEach(filterSet.add, filterSet);

    return Array.from(filterSet).sort(this.comparar).slice(0, 20);
  }

  getWeaponByName(weaponName: string) {
    const weapon = this.weapons.find(weapon => weapon.name === weaponName);
    const collection_weapons = this.weapons.filter(w => w.collection === weapon.collection);
    weapon.collection_weapons = collection_weapons;
    return weapon;
  }
